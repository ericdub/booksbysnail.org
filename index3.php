<h2>This summer, let the library come to you!</h2>
  <p>Check out library books and participate in Summer Reading through the mail, all for free.<br />
    For all students in kindergarten through sixth grade at the following schools:<br/>
    &nbsp;<br/ >
  <strong>Auxvasse, Callaway Hills, Hallsville, Harrisburg, Hatton-McCredie, Mokane, New Bloomfield, North Elementary, Sturgeon, Williamsburg</strong></p>
  <p>To find out more, call the library at 1-800-324-4806  or (573) 443-3161 or <a href="http://www.dbrl.org/contact?c=ot">e-mail the Outreach Department</a>. </p>
