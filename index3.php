   <p class="date">Tuesday, June 16, 9:30-10:15 a.m., Harrisburg Elementary School</p>
    <p>Hey, you look really stressed! Maybe you need a little clown therapy. Dr. Gesundheit prescribes a mixture of physical comedy, eccentric dance, comic juggling, wisecracks, wordplay, acrobatics, frolicking and assorted antics. Come for this therapy session and you might find yourself a volunteer in a classic comedy routine. Warning: your creativity may be required and silliness is likely to ensue. Do not be afraid&mdash;it's only clown therapy. Craig Sjogerman has been performing as Dr. G since 1979.</p>
