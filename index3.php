  <h3> Art of China and Japan</h3>
 <p class="date">Thursday, July 16, 6-7 p.m., Auxvasse Christian Church</p>
  <p>Our touch kit from the St. Louis Art Museum has cool art objects you can view and handle. We'll also give you a taste of the art of origami and serve refreshments with a Far East flair. Every child (fifth grade and under) will get a free book courtesy of the Reading Is Fundamental program.</p>
