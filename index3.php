  <p>&nbsp; </p>
  <h3>David Tower Presents: We&rsquo;re Makin&rsquo; Magic </h3>
 <p class="date">Tuesday, July 21, 1:30-2:15 p.m., Hallsville Community Center</p>
  <p>Is David Tower a scientist? A magician? A comedian? Is it real or is it illusion? Whatever the answers, you can bet that you'll have the time of your life when David Tower hits the stage. At this summer's shows, David will perform the ultimate magician's creative challenge: performing a professional magic show using only items purchased from a local department store. Can he do it? Ages 5 and older will enjoy finding out.</p>
